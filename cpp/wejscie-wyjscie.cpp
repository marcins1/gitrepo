#include <iostream>
using namespace std;
int main()
{	
	float a,b;
    
    cout<<"Podaj 1 liczbę: ";
    cin>>a;
    cout<<a<<endl;
    
    cout<<"Podaj 2 liczbę: ";
    cin>>b;
    cout<<b<<endl;

    cout<<"Suma liczb a i b wynosi: "<<a+b<<endl;
    cout<<"Roznica liczb a i b wynosi: "<<a-b<<endl;
    cout<<"Iloczyn liczb a i b wynosi: "<<a*b<<endl;
    cout<<"Iloraz liczb a i b wynosi: "<<a/b<<endl;
	
	return 0;
}

